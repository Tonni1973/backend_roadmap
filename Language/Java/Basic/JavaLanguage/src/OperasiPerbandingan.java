public class OperasiPerbandingan {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 50;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(b == (c - (b + a)));
        System.out.println(c >= b);
        System.out.println(a >= b);
        System.out.println(a != b);
    }
}
